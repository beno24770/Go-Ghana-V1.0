// Helper functions
export function getFestivalsByRegion(region: string): Festival[] {
    return GHANA_FESTIVALS.filter(f => f.region.toLowerCase().includes(region.toLowerCase()));
}

export function getFestivalById(id: string): Festival | undefined {
    return GHANA_FESTIVALS.find(f => f.id === id);
}

export function searchFestivals(query: string): Festival[] {
    const lowerQuery = query.toLowerCase();
    return GHANA_FESTIVALS.filter(f => 
        f.name.toLowerCase().includes(lowerQuery) ||
        f.description.toLowerCase().includes(lowerQuery) ||
        f.location.toLowerCase().includes(lowerQuery)
    );
}

